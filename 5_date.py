# 5.	Тривалість роботи програми

# Напишіть функцію, яка обчислює тривалість виконання програми
# (початок і кінець зафіксовані як мітки часу).

import time


def program_duration():
    """
    Обчислює тривалість виконання програми.

    Початок і кінець фіксуються як мітки часу.
    """
    # Фіксуємо час початку
    start_time = time.time()

    # Код програми (замініть на свій)
    print("Виконується програма...")
    time.sleep(2)  # Симуляція затримки (замість неї використовуйте ваш код)

    # Фіксуємо час завершення
    end_time = time.time()

    # Обчислюємо тривалість
    duration = end_time - start_time
    print(f"Програма виконувалась {duration:.2f} секунд.")


# Виклик функції
program_duration()
