# 8.	Пошук найменшого спільного кратного (НСК)

# Реалізуйте функцію, яка знаходить НСК двох чисел за допомогою math.gcd.

import math

def lcm(a, b):
    """
    Знаходить найменше спільне кратне (НСК) двох чисел.

    :param a: Перше число (ціле)
    :param b: Друге число (ціле)
    :return: НСК двох чисел
    """
    if a == 0 or b == 0:
        return 0  # Якщо одне з чисел дорівнює нулю, НСК = 0
    return abs(a * b) // math.gcd(a, b)

# Приклад використання
num1 = 12
num2 = 15
print(f"НСК чисел {num1} і {num2}: {lcm(num1, num2)}")