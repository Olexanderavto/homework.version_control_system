# 5.	Визначення числа ітерацій логарифма
# Напишіть функцію, яка рахує, скільки разів потрібно взяти натуральний логарифм числа,
# щоб отримати значення менше 1.

import math

def count_log_iterations(n):
    """
    Рахує кількість ітерацій натурального логарифма, необхідних для того,
    щоб значення стало меншим за 1.

    :param n: Число (повинно бути більше 0)
    :return: Кількість ітерацій
    """
    if n <= 0:
        raise ValueError("Число повинно бути більше 0")

    iterations = 0
    while n >= 1:
        n = math.log(n)
        iterations += 1

    return iterations

# Приклад використання
number = 20  # Замініть на ваше число
print(f"Кількість ітерацій для числа {number}: {count_log_iterations(number)}")